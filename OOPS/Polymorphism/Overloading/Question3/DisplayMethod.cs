namespace Question3
{
    public class DisplayMethod
    {
        public int Display(int num)
        {
            return num*num;
        }
        public float Display(float num1)
        {
            return num1*num1;
        }
        public double Display(double num2)
        {
            return num2*num2;
        }
        public long Display(long num3)
        {
            return num3*num3;
        }
    }
}